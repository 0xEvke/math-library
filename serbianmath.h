#include<stdio.h>

void sabiranje(int num1, int num2) {
        int rezultat = num1+num2;
        printf(rezultat);
}

void oduzimanje(int num1, int num2) {
        int rezultat = num1-num2;
        printf(rezultat);
}

void deljenje(int num1, int num2) {
        int rezultat = num1/num2;
        printf(rezultat);
}

void mnozenje(int num1, int num2) {
        int rezultat = num1*num2;
        printf(rezultat);
}

void ostatak(int num1, int num2) {
        int rezultat = num1%num2;
        printf(rezultat);
}

