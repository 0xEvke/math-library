int sabiranje(int num1, int num2) {
        return num1 + num2;
}

int oduzimanje(int num1, int num2) {
        return num1 - num2;
}

int deljenje(int num1, int num2) {
        return num1 / num2;
}

int mnozenje(int num1, int num2) {
        return num1 * num2;
}

int ostatak(int num1, int num2) {
        return num1 % num2;
}

